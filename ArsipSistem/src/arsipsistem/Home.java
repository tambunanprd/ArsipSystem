     public void loadTabelProduk(String teks){
       
        String sql = "SELECT * FROM arsip WHERE username like '%"+teks+"%'";
        Object[] kolom = {"ID", "Username", "File"};
        DefaultTableModel dataModel = new DefaultTableModel(null, kolom);
        tbProdukk.setModel(dataModel);
        tbProdukk.getColumnModel().getColumn(0).setMaxWidth(30);
        try{
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()){
                int id = rs.getInt("id_file");
                String nama = rs.getString("username");
                String harga = rs.getString("file");
                
                Object[] data={id,nama,harga};
                dataModel.addRow(data);
            }
         } catch (SQLException e){
             System.out.println(e.getMessage());
         }
        
    }
