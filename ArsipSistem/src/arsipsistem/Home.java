    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        if(tbProdukk.isRowSelected(tbProdukk.getSelectedRow())==true){
        selectedID = (int) tbProdukk.getValueAt(tbProdukk.getSelectedRow(),0);
        JTextField txtNama = new JTextField(15);
        JTextField txtHarga = new JTextField(15);
        txtNama.setText(tbProdukk.getValueAt(tbProdukk.getSelectedRow(),1).toString());
        txtHarga.setText(tbProdukk.getValueAt(tbProdukk.getSelectedRow(),2).toString());
        JPanel myPanel = new JPanel();
        myPanel.setLayout(new GridLayout(2,2,0,10));
        myPanel.add(new JLabel ("Username"));
        myPanel.add(txtNama);
        myPanel.add(new JLabel("File"));
        myPanel.add(txtHarga);
        
        int result = JOptionPane.showConfirmDialog(this, myPanel,
                "Ubah file",
                JOptionPane.OK_CANCEL_OPTION,JOptionPane.PLAIN_MESSAGE);
        if (result == JOptionPane.OK_OPTION){
            try{
                Statement stmt = conn.createStatement();
                stmt.executeUpdate("UPDATE arsip SET username='"
                + txtNama.getText()+"', file='"
                + txtHarga.getText() + "'WHERE id_file='"
                + selectedID+"'");
            loadTabelProduk();
            }catch (SQLException e){
                System.out.println(e.getMessage());
            }
        }
        } else {
            JOptionPane.showConfirmDialog(this,"Tidak ada yang dipilih");
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if(tbProdukk.isRowSelected(tbProdukk.getSelectedRow())==true){
        selectedID = (int) tbProdukk.getValueAt(tbProdukk.getSelectedRow(),0);
        int result = JOptionPane.showConfirmDialog(
                this,
                "Kamu akan menghapus data" + tbProdukk.getValueAt(tbProdukk.getSelectedRow(),1).toString(),"Peringatan!",JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
                );
        if (result == JOptionPane.YES_OPTION){
            try {
                Statement stmt = conn.createStatement();
                stmt.executeUpdate("DELETE FROM arsip WHERE id_file='"
                + selectedID + "'");
                
                loadTabelProduk();
            } catch (SQLException e){
                System.out.println(e.getMessage());
            }
        }
         else {
                JOptionPane.showConfirmDialog(this,"Tidak ada yang dipilih");
                }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

